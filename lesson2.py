num1 = 10
num2 = 20
  

#addition

print(str(num1)+  " + " +  str(num2) + " = " + str(int(num1)+ int(num2)))

#multiplication

print(str(num1)+  " * " +  str(num2) + " = " + str(int(num1) * int(num2)))

#division

print(str(num1)+  " / " +  str(num2) + " = " + str(int(num1) / int(num2)))

#subtraction

print(str(num2)+  " - " +  str(num1) + " = " + str(int(num2) - int(num1)))




