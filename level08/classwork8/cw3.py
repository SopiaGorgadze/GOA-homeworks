#  2) დაწერეთ and და or ლოგიკური ოპერატორების ყველა შესაძლო გზა ამ დავალებაში გამოიყენეთ შედარების ოპერატორები
print(5 > 3 and 10 < 20) #true
print(5 > 3 and 10 > 20) #false
print(10 > 20 and 5 > 3) #false
print(10 > 20 and 10 > 20) #false

print(5 > 3 or 10 < 20) #true
print(5 > 3 or 10 > 20) #true
print(10 > 20 or 5 > 3) #true
print(10 > 20 or 10 > 20) #false